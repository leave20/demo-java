public class DoublePrimitive {
    public static void main(String[] args) {
        double realDouble = 3.4028235E39;
        System.out.println("realDouble= " + realDouble);
        System.out.println("number in byte: " + Double.BYTES);
        System.out.println("number in bites: " + Double.SIZE);
        System.out.println("max value in double: " + Double.MAX_VALUE);
        System.out.println("min value in float: " + Double.MIN_VALUE);
    }
}
