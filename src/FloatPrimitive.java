public class FloatPrimitive {
    public static void main(String[] args) {
        float realFloat=0.00000000015f;
        System.out.println("realFloat=" + realFloat);
        System.out.println("number in byte: "+ Float.BYTES);
        System.out.println("number in bites: "+ Float.SIZE);
        System.out.println("max value in float: "+ Float.MAX_VALUE);
        System.out.println("min value in float: "+ Float.MIN_VALUE);
    }
}
